#include<iostream>
using namespace std;
int main(){
	short s;
	s = 12;
	cout<<"Short Value = "<<s<<endl;
	
	int a;
	a = 100;
	cout<<"Integer Value =  "<<a<<endl;
	
	long l;
	l = 134245;
	cout<<"Long Value = "<<l<<endl;
	
	float f;
	f = 12.22;
	cout<<"Floating Value =  "<<f<<endl;
	
	double d;
	d = 12.34625;
	cout<<"Double Value = "<<d<<endl;
	
	char c;
	c = 'G';
	cout<<"Character Value =  "<<c<<endl;
	
	bool b;
	b = 1;
	cout<<"Boolean Value = "<<b<<endl;
	
	long double ld;
	ld = 1234.44255;
	cout<<"Long Double value = "<<ld<<endl;
	
	wchar_t w;
	w = L'A';
	cout<<"Wide Character Value = "<<w<<endl;
}
